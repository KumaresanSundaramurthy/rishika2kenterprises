            e.preventDefault;
            $(".AllFirstPreview").removeClass('d-none');
            $(this).closest('div').addClass('d-none');
        });

        $("#ShowSecondPreview").click(function(e) {
            e.preventDefault;
            $(".AllSecondPreview").removeClass('d-none');
            $(this).closest('div').addClass('d-none');
        });

